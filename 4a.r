# Mathematical Operators
a <- 5 + 3  # Addition
b <- 10 - 4  # Subtraction
c <- 3 * 7  # Multiplication
d <- 15 / 5  # Division
e <- 2 ^ 3  # Exponentiation
f <- 6 %% 8  # Modulus (remainder)

# Printing the results
print(a)
print(b)
print(c)
print(d)
print(e)

# Relational Operators
# Equality
x <- 5 == 5
print(x)

# Inequality
y <- 3 != 7
print(y)

# Greater than
m <- 10 > 8
print(m)

# Less than
n <- 5 < 2
print(n)

# Greater than or equal to
p <- 6 >= 6
print(p)

# Less than or equal to
q <- 4 <= 3
print(q)

# Logical Operators
a <- TRUE
b <- FALSE

# Logical AND
var1 <- a & b
print(var1)

# Logical OR
var2 <- a | b
print(var2)

# Logical NOT
var3 <- !a
print(var3)
